
public class WrapperClasses {

	public static void main(String[] args) {
		
		String  s = "100";
		
		
		System.out.println(s+20);
		
		
		int i = Integer.parseInt(s);
		System.out.println(i+20);
		
		
		System.out.println("********************");
		
		String s1 = "False";
		
		boolean b = Boolean.parseBoolean(s1);
		
		System.out.println(b);
		
		

	}

}
